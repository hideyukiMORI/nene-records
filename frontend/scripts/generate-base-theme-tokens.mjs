// Extracts the per-mode CSS token sets of the built-in public themes into data,
// so the admin "save customization as a runtime theme" flow can resolve a full
// manifest without loading the public theme CSS (#454).
//
// Each built-in theme defines all contract tokens under `[data-theme='id']` and
// `[data-theme='id-dark']`. We parse those root blocks (not descendant rules)
// into { id: { light: {...}, dark: {...} } } and write a typed module.
//
// Usage: npm run gen:base-tokens

import { readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const themesDir = join(here, '../src/shared/ui/theme/themes')
const outPath = join(here, '../src/shared/lib/base-theme-tokens.ts')

/** Match a ROOT token block `[data-theme='id'] { ... }` (no descendant selector). */
const BLOCK = /\[data-theme='([a-z0-9-]+)'\]\s*\{([^}]*)\}/g

/** @type {Record<string, { light: Record<string,string>, dark: Record<string,string> }>} */
const themes = {}

const files = readdirSync(themesDir)
  .filter((f) => f.endsWith('.css') && !f.endsWith('.components.css') && f !== 'admin-themes.css')
  .sort()

for (const file of files) {
  const css = readFileSync(join(themesDir, file), 'utf8')
  for (const match of css.matchAll(BLOCK)) {
    const selector = match[1]
    const body = match[2]
    const isDark = selector.endsWith('-dark')
    const id = isDark ? selector.slice(0, -'-dark'.length) : selector

    /** @type {Record<string,string>} */
    const tokens = {}
    // Strip CSS comments first — otherwise a `/* … */` before a declaration
    // glues onto the next token name (split is on ';') and the token is dropped.
    const cleanBody = body.replace(/\/\*[\s\S]*?\*\//g, '')
    for (const decl of cleanBody.split(';')) {
      const idx = decl.indexOf(':')
      if (idx === -1) continue
      const name = decl.slice(0, idx).trim()
      if (!name.startsWith('--')) continue
      const value = decl
        .slice(idx + 1)
        .trim()
        .replace(/\s+/g, ' ')
      if (value === '') continue
      tokens[name.slice(2)] = value // drop the leading "--" → manifest token key
    }
    if (Object.keys(tokens).length === 0) continue

    // Built-in themes set `color-scheme` as a plain CSS property (not a --var),
    // but the manifest contract requires a `color-scheme` token per mode. Inject
    // it deterministically from the block's mode so the captured base is complete.
    tokens['color-scheme'] = isDark ? 'dark' : 'light'

    themes[id] ??= { light: {}, dark: {} }
    themes[id][isDark ? 'dark' : 'light'] = tokens
  }
}

// Keep only themes that have BOTH modes populated (a usable base).
const complete = Object.fromEntries(
  Object.entries(themes)
    .filter(([, t]) => Object.keys(t.light).length > 0 && Object.keys(t.dark).length > 0)
    .sort(([a], [b]) => a.localeCompare(b)),
)

const header = `// AUTO-GENERATED by scripts/generate-base-theme-tokens.mjs — do not edit by hand.
// Regenerate with: npm run gen:base-tokens
//
// Per-mode CSS token sets of the built-in public themes, extracted from their
// [data-theme] blocks. Used to resolve a full runtime-theme manifest when an
// admin saves a customization as a new theme (#454).
`

const body = `export interface BaseThemeTokens {
  light: Record<string, string>
  dark: Record<string, string>
}

export const BASE_THEME_TOKENS: Record<string, BaseThemeTokens> = ${JSON.stringify(complete, null, 2)}
`

writeFileSync(outPath, `${header}\n${body}`)
const ids = Object.keys(complete)
console.log(`Wrote ${ids.length} base themes to ${outPath}`)
console.log(`  ${ids.join(', ')}`)
